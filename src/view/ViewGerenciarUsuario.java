    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        if (tableUsuarios.getSelectedRow() != -1) {
            if ((txtCadastroLogin.getText() == null || txtCadastroPwd1.getPassword() == null) || txtCadastroLogin.getText() == "") {
                JOptionPane.showMessageDialog(null, "É necessário ter login e senha ao menos");
            } else {
                if (Arrays.equals(txtCadastroPwd1.getPassword(), txtCadastroPwd2.getPassword())) {
                    int adm;
                    if (rdbAdm.isSelected()) {
                        adm = 1;
                    } else {
                        adm = 0;
                    }
                    String pass = new String(txtCadastroPwd1.getPassword());
                    Usuarios u;
                    u = new Usuarios(txtCadastroLogin.getText(), pass, adm);
                    UsuariosDAO dao = new UsuariosDAO();
                    u.setFuncao((String)cbFuncao.getSelectedItem());
                    u.setNome_completo(txtCadastroNome.getText());
                    dao.Update(u, (String) tableUsuarios.getValueAt(tableUsuarios.getSelectedRow(), 0));
                } else {
                    JOptionPane.showMessageDialog(null, "As senhas não coincidem");
                }
            }
            ReadJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void tableUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableUsuariosMouseClicked
        // TODO add your handling code here:

        if (tableUsuarios.getSelectedRow() != -1) {
            cbFuncao.setSelectedItem(tableUsuarios.getValueAt(tableUsuarios.getSelectedRow(), 4).toString());
            txtCadastroLogin.setText(tableUsuarios.getValueAt(tableUsuarios.getSelectedRow(), 0).toString());
            txtCadastroNome.setText(tableUsuarios.getValueAt(tableUsuarios.getSelectedRow(), 3).toString());
            txtCadastroPwd1.setText(tableUsuarios.getValueAt(tableUsuarios.getSelectedRow(), 1).toString());
            txtCadastroPwd2.setText(tableUsuarios.getValueAt(tableUsuarios.getSelectedRow(), 1).toString());
            if ((int) (tableUsuarios.getValueAt(tableUsuarios.getSelectedRow(), 2)) == 1) {
                rdbAdm.setSelected(true);
            } else {
                btnGrpAdm.clearSelection();
            }

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
        }
    }//GEN-LAST:event_tableUsuariosMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        if (txtExcluirLogin.getText() != null && txtExcluirLogin.getText() != "") {
            Usuarios u = new Usuarios();
            UsuariosDAO udao = new UsuariosDAO();

            u.setLogin(txtExcluirLogin.getText());

            udao.Delete(u);
            ReadJTable();
        } else {
            JOptionPane.showMessageDialog(null, "Campo vazio!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

