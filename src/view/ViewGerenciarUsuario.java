    private void ReadJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tableUsuarios.getModel();
        UsuariosDAO udao = new UsuariosDAO();
        modelo.setNumRows(0);
        for (Usuarios u : udao.Read()) {
            modelo.addRow(new Object[]{
                u.getLogin(),
                u.getSenha(),
                u.getHierarquia(),
                u.getNome_completo(),
                u.getFuncao()
            });
        }
    }

    private void btnCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastroActionPerformed
        // TODO add your handling code here:
        if ((txtCadastroLogin.getText() == null || txtCadastroPwd1.getPassword() == null) || txtCadastroLogin.getText() == "") {
            JOptionPane.showMessageDialog(null, "É necessário ter login e senha ao menos");
        } else {
            if (Arrays.equals(txtCadastroPwd1.getPassword(), txtCadastroPwd2.getPassword())) {
                int adm;
                if (rdbAdm.isSelected()) {
                    adm = 1;
                } else {
                    adm = 0;
                }
                String pass = new String(txtCadastroPwd1.getPassword());
                Usuarios u = new Usuarios(txtCadastroLogin.getText(), pass, adm);
                UsuariosDAO dao = new UsuariosDAO();
                u.setFuncao((String)cbFuncao.getSelectedItem());
                u.setNome_completo(txtCadastroNome.getText());
                dao.Create(u);
            } else {
                JOptionPane.showMessageDialog(null, "As senhas não coincidem");
            }
        }
        ReadJTable();
    }//GEN-LAST:event_btnCadastroActionPerformed

