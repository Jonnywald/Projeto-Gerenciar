    public void ReadTableHist() {
        DefaultTableModel modelo = (DefaultTableModel) tableHist.getModel();
        TransferenciaDAO tdao = new TransferenciaDAO();
        ClienteDAO cdao = new ClienteDAO();
        modelo.setNumRows(0);
        for (Transferencia t : tdao.Read()) {
            modelo.addRow(new Object[]{
                t.getID(),
                cdao.GetCliente(t.getCodCliente()),
                t.getValor(),
                t.getDataTransf()
            });
        }
    }
    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        if (tableHist.getSelectedRow() != -1) {
            TransferenciaDAO tdao = new TransferenciaDAO();
            tdao.Delete((int) tableHist.getValueAt(tableHist.getSelectedRow(), 0));
            ReadTableHist();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
        }
        ReadTableHist();
    }//GEN-LAST:event_btnExcluirActionPerformed

