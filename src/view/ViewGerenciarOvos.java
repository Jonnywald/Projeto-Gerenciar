    private void btnTipoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTipoSalvarActionPerformed
        // TODO add your handling code here:
        if (txtTipoDesc.getText() != null && txtTipoDesc.getText() != "" && txtTipoCod.getText() != null && txtTipoCod.getText() != "") {
            Ovos_TipoDAO odao = new Ovos_TipoDAO();
            Ovos_Tipo tOvo = new Ovos_Tipo();
            tOvo.setTipo(txtTipoDesc.getText());
            tOvo.setID(Integer.parseInt(txtTipoCod.getText()));
            tOvo.setPreco(Double.parseDouble(txtPreco.getText()));
            if (odao.Check(tOvo.getID())) {
                int input = JOptionPane.showConfirmDialog(null, "Codigo já cadastrado, deseja atulalizar item?");
                if (input == 0) {
                    odao.Update(tOvo);
                }
            } else {
                odao.Create(tOvo);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Campo Vazio!");
        }
        ReadJTableTipo();
    }//GEN-LAST:event_btnTipoSalvarActionPerformed

