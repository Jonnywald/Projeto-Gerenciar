    private void ReadJTableTipo() {
        DefaultTableModel modelo = (DefaultTableModel) tableTipo.getModel();
        Ovos_TipoDAO odao = new Ovos_TipoDAO();
        modelo.setNumRows(0);
        for (Ovos_Tipo o : odao.Read()) {
            modelo.addRow(new Object[]{
                o.getID(),
                o.getTipo(),
                o.getPreco()
            });
        }
    }
    private void ReadJTableOvo() {
        DefaultTableModel modelo = (DefaultTableModel) tableOvo.getModel();
        Ovos_CaipiraDAO odao = new Ovos_CaipiraDAO();
        modelo.setNumRows(0);
        for (Ovos_Caipira o : odao.Read()) {
            modelo.addRow(new Object[]{
                o.getLote(),
                o.getTipo_ovo(),
                o.getQtd(),
                o.getData_embalagem(),
                o.calcPrecoTotal()
            });
        }
    }
    private void btnTipoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTipoExcluirActionPerformed
        // TODO add your handling code here:
        if (tableTipo.getSelectedRow() != -1) {
            Ovos_TipoDAO odao = new Ovos_TipoDAO();
            odao.Delete((int) tableTipo.getValueAt(tableTipo.getSelectedRow(), 0));
            ReadJTableTipo();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
        }
        ReadJTableTipo();
    }//GEN-LAST:event_btnTipoExcluirActionPerformed

    private void btnOvoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOvoSalvarActionPerformed
        // TODO add your handling code here:
        Ovos_Tipo tOvo = new Ovos_Tipo();
        tOvo = (Ovos_Tipo) cbTipo.getSelectedItem();
        Ovos_Caipira o = new Ovos_Caipira();
        Ovos_CaipiraDAO odao = new Ovos_CaipiraDAO();
        o.setQtd(Integer.parseInt(txtQtd.getText()));
        o.setTipo_ovo(tOvo);
        o.setData_embalagem(((JTextField)txtData.getDateEditor().getUiComponent()).getText());
        o.setLote(Integer.parseInt(txtLote.getText()));
        if (txtQtd.getText() != "" && txtData.getDate() != null && txtLote.getText() != "") {
            if(odao.Check(o.getLote())){
                int input = JOptionPane.showConfirmDialog(null, "Codigo já cadastrado, deseja atulalizar item?");
                if (input == 0) {
                    odao.Update(o);
                }
            }else{
                odao.Create(o);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Campo Vazio!");
        }
        ReadJTableOvo();
    }//GEN-LAST:event_btnOvoSalvarActionPerformed

